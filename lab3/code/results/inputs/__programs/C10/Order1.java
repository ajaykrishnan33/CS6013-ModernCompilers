class Order1 {
    public static void main(String[] a ) {
        int ab;
        int cd;
        int xy;
        int ten;
        ab = 5;
        cd = 5;
        ten = 10;
        for(xy=ab+cd;xy<ten;xy=ab+cd){
            ab=0;
            xy=8;
        }
        System.out.println(xy);
        System.out.println(ab);
        System.out.println(cd); 
    }
}
